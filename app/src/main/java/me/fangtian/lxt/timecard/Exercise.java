public class Exercise {

    private String classid;
    private String classname;
    private String answerpic;
    private String stdid;
    private String stdname;
    private String qid;
    private String clid;
    private String worktime;

    public Exercise(){

    }

    public String getClassid() { return classid; }
    public String getClassname() { return classname; }
    public String getAnswerpic() { return answerpic; }
    public String getStdid() { return stdid; }
    public String getStdname() { return stdname; }
    public String getQid() { return qid; }
    public String getClid() { return clid; }
    public String getWorktime() { return worktime; }

    public void setClassid(String classid) { this.classid = classid; }
    public void setClassname(String classname) { this.classname = classname; }
    public void setAnswerpic(String answerpic) { this.answerpic = answerpic; }
    public void setStdid(String stdid) { this.stdid = stdid; }
    public void setStdname(String stdname) { this.stdname = stdname; }
    public void setQid(String qid) { this.qid = qid; }
    public void setClid(String clid) { this.clid = clid; }
    public void setWorktime(String worktime) { this.worktime = worktime; }

}
